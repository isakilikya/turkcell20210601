package com.training.spring.language.v3;


public class LangProp {

    private String shortName;
    private String name;
    private String desc;
    private String className;

    public String getShortName() {
        return this.shortName;
    }

    public void setShortName(final String shortNameParam) {
        this.shortName = shortNameParam;
    }

    public String getName() {
        return this.name;
    }

    public void setName(final String nameParam) {
        this.name = nameParam;
    }

    public String getDesc() {
        return this.desc;
    }

    public void setDesc(final String descParam) {
        this.desc = descParam;
    }

    public String getClassName() {
        return this.className;
    }

    public void setClassName(final String classNameParam) {
        this.className = classNameParam;
    }


}
